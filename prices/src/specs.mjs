// Извлечение характеристик товара из свободного названия.
//
// Зачем: поиск маркетплейса на запрос «Лампа LED A60 E27 3000K 11Вт IEK»
// возвращает 100 товаров, среди которых нет НИ ОДНОГО подходящего — движок
// игнорирует характеристики и набивает выдачу похожим мусором. Поэтому
// характеристики приходится вытаскивать самим и сравнивать самим.
//
// Всё работает поверх canon() — ЗАГЛАВНЫЙ текст со свёрнутыми омоглифами.
// Словесные шаблоны строятся через тот же canon(), чтобы не подбирать вручную,
// во что превратится «тёплый» после свёртки (спойлер: «TEПЛЫЙ», где T и E уже
// латинские, а П и Л — нет).

import { canon } from './normalize.mjs';

const W = (s) => canon(s); // шаблон-слово в том же виде, что и разбираемый текст

/** Цветовая температура словами — продавцы часто пишут только их. */
const CCT_WORDS = [
  { re: new RegExp(W('тепл')), k: 3000 },
  { re: new RegExp(W('нейтральн')), k: 4000 },
  { re: new RegExp(W('дневн')), k: 4000 },
  { re: new RegExp(W('холодн')), k: 6500 },
];

/**
 * Форма колбы словом. Значения — «семейства», а не точный код: продавец пишет
 * «груша», мы знаем только что это A-серия. Поэтому shape тут без числа
 * («A-ГРУША»), и матчер сверяет семейство, а не точный размер. Реальный A60,
 * названный «грушей», раньше терял форму и висел в «под вопросом».
 */
const SHAPE_WORDS = [
  { re: new RegExp(W('груша')), shape: 'A-ГРУША' },
  { re: new RegExp(W('свеча')), shape: 'C-СВЕЧА' },
  { re: new RegExp(`${W('шар')}|${W('шаров')}`), shape: 'G-ШАР' },
  { re: new RegExp(`${W('рефлектор')}|${W('зеркальн')}`), shape: 'R-РЕФЛ' },
];

/** К какому «семейству» относится точный код формы (A60 → A-ГРУША). */
export function shapeFamily(shape) {
  if (!shape) return null;
  const letter = shape.match(/^[A-Z]+/)?.[0];
  return { A: 'A-ГРУША', C: 'C-СВЕЧА', G: 'G-ШАР', R: 'R-РЕФЛ' }[letter] ?? null;
}

/**
 * Разбирает название в набор характеристик.
 * Отсутствующая характеристика = undefined (не 0 и не null) — это важно:
 * «не указано» и «указано другое» обрабатываются по-разному.
 */
export function extractSpecs(text) {
  const c = canon(text);
  const s = {};

  // Цоколь: E27 / E14 / E40, GU10, GU5.3, G4 / G9 / G13.
  // G с 1-2 цифрами до 13 — цоколь; G45/G95/G120 — форма колбы, ловится ниже.
  let m = c.match(/\bE\s?(14|27|40)\b/);
  if (m) s.socket = `E${m[1]}`;
  else if ((m = c.match(/\bGU\s?(10|5[.,]3)\b/))) s.socket = `GU${m[1].replace(',', '.')}`;
  else if ((m = c.match(/\bG\s?(4|9|13)\b(?!\d)/))) s.socket = `G${m[1]}`;

  // Форма колбы: A60, C37, R63, ST64, G45, MR16, PAR16.
  if ((m = c.match(/\b(A|C|R|ST|MR|PAR)\s?(\d{2,3})\b/))) s.shape = `${m[1]}${m[2]}`;
  else if ((m = c.match(/\bG\s?(\d{2,3})\b/))) s.shape = `G${m[1]}`;
  else {
    // Форма словом. Продавцы часто пишут «груша» вместо «A60» — без этого
    // реальные лампы висли в «под вопросом» на 67% (не указана форма).
    // Код без числа: «A60»/«груша» = «форма A-груша», размер не сверяем.
    const byWord = SHAPE_WORDS.find((w) => w.re.test(c));
    if (byWord) s.shape = byWord.shape;
  }

  // Мощность, Вт. canon превращает «11Вт» в «11BT», «11 W» остаётся «11 W».
  if ((m = c.match(/\b(\d{1,3}(?:[.,]\d)?)\s*(?:BT|W)\b/))) {
    s.power = parseFloat(m[1].replace(',', '.'));
  }

  // Цветовая температура: сначала цифрами, иначе словами.
  if ((m = c.match(/\b([2-9]\d{3})\s*K\b/))) s.cct = parseInt(m[1], 10);
  else {
    const hit = CCT_WORDS.find((w) => w.re.test(c));
    if (hit) s.cct = hit.k;
  }

  // Световой поток, Лм. canon('лм') = 'ЛM' (Л кириллическая, M латинская).
  if ((m = c.match(new RegExp(`\\b(\\d{2,5})\\s*(?:${W('лм')}|LM)\\b`)))) {
    s.lumens = parseInt(m[1], 10);
  }

  // Напряжение, В: «230В»→«230B», «220-240В», «12V».
  if ((m = c.match(/\b(\d{2,3})(?:\s*-\s*\d{2,3})?\s*[BV]\b/))) s.voltage = parseInt(m[1], 10);

  // Ток, А: «16А»→«16A». Цифры идут ПЕРЕД буквой, поэтому с формой A60 не путается.
  if ((m = c.match(/\b(\d{1,2})\s*A\b/))) s.current = parseInt(m[1], 10);

  // Степень защиты.
  if ((m = c.match(/\bIP\s?(\d{2})\b/))) s.ip = `IP${m[1]}`;

  // Количество клавиш / постов — для выключателей и рамок.
  if ((m = c.match(new RegExp(`(\\d)\\s*-?\\s*${W('клавиш')}`)))) s.keys = parseInt(m[1], 10);
  if ((m = c.match(new RegExp(`(\\d)\\s*-?\\s*${W('пост')}`)))) s.posts = parseInt(m[1], 10);

  return s;
}

/**
 * Сколько штук в лоте. Продавцы сплошь торгуют связками: «2шт», «комплект 10».
 * Цена на витрине — за ВЕСЬ лот, поэтому без этого числа сравнение с закупочной
 * врёт кратно: пачка из 10 ламп за 300 ₽ выглядит как лампа за 300 ₽, и бот
 * молча проходит мимо находки по 30 ₽/шт.
 *
 * Это не характеристика товара, а делитель цены — сравнивать её с эталоном
 * нельзя (наш эталон всегда «за штуку»), поэтому она живёт отдельно.
 */
export function extractPack(text) {
  const c = canon(text);
  let m =
    c.match(new RegExp(`(\\d{1,3})\\s*(?:${W('шт')}|${W('штук')})`)) ||
    c.match(new RegExp(`${W('комплект')}\\s*(?:${W('из')}\\s*)?(\\d{1,3})`)) ||
    c.match(new RegExp(`${W('набор')}\\s*(?:${W('из')}\\s*)?(\\d{1,3})`)) ||
    c.match(new RegExp(`${W('упаковка')}\\s*(\\d{1,3})`)) ||
    c.match(/\bX\s?(\d{1,2})\b/); // «лампа x10»

  const qty = m ? parseInt(m[1], 10) : 1;
  // Лот больше сотни — почти наверняка мы неверно прочли название, а не опт.
  return qty >= 1 && qty <= 100 ? qty : 1;
}

/** Характеристики, по которым расхождение = точно другой товар. */
export const HARD_SPECS = ['socket', 'shape', 'power', 'cct', 'current', 'keys', 'posts'];

/** Вес характеристики при подсчёте уверенности. */
export const SPEC_WEIGHT = {
  socket: 3, shape: 3, power: 3, cct: 2,
  current: 2, keys: 2, posts: 2, lumens: 1, voltage: 1, ip: 1,
};
