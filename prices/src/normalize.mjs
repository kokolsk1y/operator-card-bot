// Нормализация текста названий товаров перед разбором характеристик.
//
// Главная проблема: продавцы вперемешку пишут кириллицу и латиницу в кодах.
// «Е27» кириллической Е и «E27» латинской E выглядят одинаково, но это разные
// строки — сравнение в лоб их не сматчит. То же с «А60»/«A60», «3000К»/«3000K»,
// «230В»/«230B». Встречается даже в пределах одного названия.
//
// Решение: сначала ВЕРХНИЙ регистр, потом свёртка кириллических омоглифов в
// латиницу. В верхнем регистре набор омоглифов ровно определён (12 букв), в
// нижнем — нет: «т» не похожа на «t», а «Т» на «T» похожа. Поэтому регистр
// поднимаем всегда, и все разборщики характеристик работают с ЗАГЛАВНОЙ латиницей.

/** Кириллические заглавные, визуально неотличимые от латинских. */
const HOMOGLYPHS = {
  А: 'A', В: 'B', Е: 'E', К: 'K', М: 'M', Н: 'H',
  О: 'O', Р: 'P', С: 'C', Т: 'T', У: 'Y', Х: 'X',
};

const HOMOGLYPH_RE = new RegExp(`[${Object.keys(HOMOGLYPHS).join('')}]`, 'g');

/**
 * Приводит название к каноничному виду для разбора характеристик.
 * Результат — ЗАГЛАВНЫЙ текст, где коды гарантированно в латинице.
 *
 * «Лампа A60 Е27 3000К 11Вт» → «ЛAMПA A60 E27 3000K 11BT»
 *  (кириллические Е, К, А и В стали латинскими — дальше их ловит один regex)
 */
export function canon(text) {
  if (!text) return '';
  return String(text)
    .toUpperCase()
    .replace(/Ё/g, 'Е')          // до свёртки: иначе Ё→кириллическая Е проскочит мимо неё
    .replace(HOMOGLYPH_RE, (ch) => HOMOGLYPHS[ch])
    .replace(/[«»"'`]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

/**
 * Каноничный вид артикула: только буквы и цифры, латиница, верхний регистр.
 * «LLE-A60-11-230-30-E27» и «lle a60 11 230 30 e27» → оба «LLEA601123030E27».
 * Нужно, потому что продавцы пишут артикул через дефис, пробел или слитно.
 */
export function canonArticle(text) {
  return canon(text).replace(/[^A-Z0-9]/g, '');
}

/** Отбрасывает Ё/Й-различия и лишнее для нечёткого сравнения слов. */
export function words(text) {
  return canon(text)
    .replace(/[^A-Z0-9А-Я ]/g, ' ')
    .split(/\s+/)
    .filter((w) => w.length > 1);
}
